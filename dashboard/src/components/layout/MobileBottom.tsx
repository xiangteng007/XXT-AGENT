interface MobileNavItem {
    href: string;
    label: string;
    icon: React.ReactNode;
    matchPrefix?: string;
}

const mobileNav: MobileNavItem[] = [
    { href: '/', label: '首頁', icon: <HomeIcon /> },
    { href: '/social-monitor/posts', label: '社群', icon: <GlobeIcon />, matchPrefix: '/social-monitor' },
    { href: '/market/watchlist', label: '行情', icon: <MarketIcon />, matchPrefix: '/market' },
    { href: '/jobs', label: '任務', icon: <ListIcon /> },
    { href: '/settings', label: '設定', icon: <SettingsIcon /> },
];

export function MobileBottom() {
    const pathname = usePathname();

    const isActive = (item: MobileNavItem) => {
        if (item.matchPrefix) {
            return pathname.startsWith(item.matchPrefix);
        }
        if (item.href === '/') return pathname === '/';
        return pathname.startsWith(item.href);
    };

    return (
        <>
            {mobileNav.map(item => (
                <Link
                    key={item.href}
                    href={item.href}
                    className={`mobile-nav-item ${isActive(item) ? 'active' : ''}`}
                >
                    {item.icon}
                    <span>{item.label}</span>
                </Link>
            ))}
        </>
    );
}

export default MobileBottom;
